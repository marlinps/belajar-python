# Operator Perbandingan 

# > lebih dari
# < Kurang dari
# >= lebih dari sama dengan
# <= kurang dari sama dengan
# == sama dengan
# != tidak sama dengan

print(1 <= 1)
print(10 == 10)
print(10 != 9)

# Operator === dan != dapat membandingkan string

print("Marlin" == "Marlin")
print("Marlin" != "Sari")